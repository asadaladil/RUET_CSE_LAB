.STACK 100H
.DATA
    PORTA EQU 00H
    PORTB EQU 02H 
    PORTC EQU 04H
    PORT_CON EQU 06H
    
.CODE
    MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
 
START:

MOV DX, PORT_CON
MOV AL,10000000B
OUT DX,AL

JMP XX
XX:

MOV AL,1
MOV DX,PORTA
OUT DX,AL
MOV CX,0DF36H
XXX:
LOOP XXX

LOOPY1:
MOV AL,0
MOV DX,PORTA
OUT DX,AL
MOV CX,0DF36H

LOOPY2:LOOP LOOPY2
JMP XX



CODE ENDS
END

